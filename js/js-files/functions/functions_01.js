// function expression
// function anonymous (Função anônima)

// number1 e number2 são chamados de parâmetros (parameters)
const sum = function (number1, number2){
  console.log(`A soma entre ${number1} + ${number2} = ${number1 + number2} `)
}

sum(2, 3) // 2 e 3 são chamados de arguments - argumentos
